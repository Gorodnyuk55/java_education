package homework.beginner.loops;
/*Write a program that print multiplication table for 5*/

public class task39 {
    public static void main(String[] args) {
        int z = 5;
        for (int i = 0; i <=10 ; i++) {
            System.out.print(z + " * "  + i + " = " + (z * i));
            System.out.println();
        }
        
    }
}
